import numpy as np
syc = np.arange(0,16).reshape(4,4)
print (syc)
print("-----------------------------")
print (syc[0]) #取第0行
print("-----------------------------")
print (syc[0:3]) #取出半开集[0,3)内的元素
print("-----------------------------")
print(syc[:3,1:4])
print("-----------------------------")
print(syc[[0,3],[2,1]])#取出（0，2） （3，1）的元素